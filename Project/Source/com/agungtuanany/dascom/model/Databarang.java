package com.agungtuanany.dascom.model;

public class Databarang {

	private String idDatabarang;
	private String namabarang;
	private int stok;
	private double hargajual;
	private double hargabeli;
	private String satuanukuran;
	private String deksripsi;
	private String keterangan;

	public final String getIdDatabarang() {
		return idDatabarang;
	}

	public final void setIdDatabarang(String idDatabarang) {
		this.idDatabarang = idDatabarang;
	}

	public final String getNamabarang() {
		return namabarang;
	}

	public final void setNamabarang(String namabarang) {
		this.namabarang = namabarang;
	}

	public final int getStok() {
		return stok;
	}

	public final void setStok(int stok) {
		this.stok = stok;
	}

	public final double getHargajual() {
		return hargajual;
	}

	public final void setHargajual(double hargajual) {
		this.hargajual = hargajual;
	}

	public final double getHargabeli() {
		return hargabeli;
	}

	public final void setHargabeli(double hargabeli) {
		this.hargabeli = hargabeli;
	}

	public final String getSatuanukuran() {
		return satuanukuran;
	}

	public final void setSatuanukuran(String satuanukuran) {
		this.satuanukuran = satuanukuran;
	}

	public final String getDeksripsi() {
		return deksripsi;
	}

	public final void setDeksripsi(String deksripsi) {
		this.deksripsi = deksripsi;
	}

	public final String getKeterangan() {
		return keterangan;
	}

	public final void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}

}
